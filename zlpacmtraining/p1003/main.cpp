#include <iostream>
#include<bits/stdc++.h>
using namespace std;
const int maxn=100005;
int a[maxn],b[maxn],g[maxn],k[maxn];
int main()
{
    int n,x,y;
    scanf("%d",&n);
    for(int i=1;i<=n;i++)
    {
      scanf("%d%d%d%d",&a[i],&b[i],&g[i],&k[i]);
    }
    scanf("%d%d",&x,&y);

    return 0;
}
